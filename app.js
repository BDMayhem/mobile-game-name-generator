let used = [];
let name = "";

document.getElementById("button").addEventListener("click", createTitle);

function createTitle(){
  name = ""
  if (used.length >= 30){
    used.forEach(function(e){
      names.push(Object.assign(e));
    });
    used = [];
  }

  const numb = Math.floor(Math.random() * 3) + 2;

  for (let i = 0; i < numb; i++){
    let sum = 0;
    names.forEach(function(e){
      sum += e.num;
    });

    chooseName(sum, i, numb)
  }
  document.getElementById("show").innerText = name;
}

function chooseName(sum, i, numb) {
  let rand = Math.floor(Math.random() * sum);

  for (let j = 0; j < names.length; j++){
    rand -= names[j].num;
    if (rand <= 0){

      if (i > 0 && names[j].placement === "first") {
        chooseName(sum, i, numb)
        break
      } else if ((i === 0 || i === numb - 1) && names[j].placement === "middle") {
        chooseName(sum, i, numb)
        break
      } else {
        name += names[j].name + " ";
        
        let chosen = names.splice(j, 1)[0];
        used.push(chosen);
        
        break;
      }
    }
  }
}
